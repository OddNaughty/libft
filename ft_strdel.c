#include "libft.h"

void	ft_strdel(char **as)
{
	int		i;

	if ((as != NULL) && (*as != NULL))
	{
		i = 0;
		while ((*as)[i])
		{
			(*as)[i] = '\0';
			i++;
		}
		free(*as);
		*as = NULL;
	}
}
