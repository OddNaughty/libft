#include "libft.h"

int		ft_strchri(const char *s, char c)
{
	unsigned int	n;

	n = 0;
	while (s[n] != '\0' && s[n] != c)
		n++;
	if (s[n] == '\0' && c != '\0')
		return (-1);
	return (n);
}
